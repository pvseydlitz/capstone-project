const mongoose = require('mongoose')

const userSchema = {
  email: {
    type: String,
    unique: true,
    required: true,
    trim: true,
  },
  username: {
    type: String,
    unique: true,
    required: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
}

module.exports = mongoose.model('User', userSchema)
